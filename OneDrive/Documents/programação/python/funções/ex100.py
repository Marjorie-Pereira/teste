lista = [1, 11, 3, 4]
print(lista)

lista.pop(2) # o numero entre parenteses representa a posição do elemento na lista
print(lista)

lista.remove(1) # o numero entre parenteses representa o número em si que será removido da lista
print(lista)

lista.append(10)
print(lista)

lista.sort() # ordem crescente
print(lista)

lista.sort(reverse=True) # ordem decrescente
print(lista)

print(lista[2])

listaNova = [10, 90, 34]
lista.append(listaNova)
print(lista)

